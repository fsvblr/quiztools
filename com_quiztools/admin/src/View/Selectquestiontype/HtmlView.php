namespace Qt\Component\Quiztools\Administrator\View\Selectquestiontype;

use Joomla\CMS\Language\Text;
use Joomla\CMS\MVC\View\GenericDataException;
use Joomla\CMS\MVC\View\HtmlView as BaseHtmlView;
use Joomla\CMS\Toolbar\ToolbarHelper;
use Qt\Component\Quiztools\Administrator\Model\SelectquestiontypeModel;

// phpcs:disable PSR1.Files.SideEffects
\defined('_JEXEC') or die;
// phpcs:enable PSR1.Files.SideEffects

/**
 * HTML View class for the QuizTools component
 *
 * @since  1.6
 */
class HtmlView extends BaseHtmlView
{
    /**
     * An array of items
     *
     * @var  array
     */
    protected $items;

    /**
     * Display the view
     *
     * @param   string  $tpl  The name of the template file to parse; automatically searches through the template paths.
     *
     * @return  void
     */
    public function display($tpl = null)
    {
        /** @var SelectquestiontypeModel $model */
        $model = $this->getModel();

        $this->items = $model->getItems();

        // Check for errors.
        if (\count($errors = $model->getErrors())) {
            throw new GenericDataException(implode("\n", $errors), 500);
        }

        $this->addToolbar();

        parent::display($tpl);
    }

    /**
     * Add the page title and toolbar.
     *
     * @return  void
     *
     * @since   1.6
     */
    protected function addToolbar()
    {
        $toolbar  = $this->getDocument()->getToolbar();

        ToolbarHelper::title(Text::_('COM_QUIZTOOLS_SELECTQUESTIONTYPE_TITLE'), 'fas fa-clipboard-question');

        $toolbar->standardButton('cancel', 'JTOOLBAR_CANCEL')
            ->onclick("location.href='index.php?option=com_quiztools&amp;view=questions'");
    }
}
